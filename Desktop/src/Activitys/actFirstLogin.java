    public void doAnimation(int pAnimation) {
        setAllToInvisible();
        new Thread() {
            public void run() {
                int x = 0;
                while (x <= 255) {
                    Color Degradation = new Color(255, 255, 255, x);
                    lblTitle.setForeground(Degradation);
                    x += 2;
                    try {
                        sleep(7);
                    } catch (InterruptedException ex) {
                    }
                }
                x = 0;
                lblSubTitle.setVisible(true);
                while (x <= 255) {
                    Color Degradation = new Color(255, 255, 255, x);
                    lblSubTitle.setForeground(Degradation);
                    x += 2;
                    try {
                        sleep(7);
                    } catch (InterruptedException ex) {
                    }
                }
                if (pAnimation == 0) {
                    x = 0;
                    btnExit.setVisible(true);
                    while (x <= 255) {
                        Color Degradation = new Color(255, 255, 255, x);
                        btnExit.setForeground(Degradation);
                        x += 2;
                        try {
                            sleep(1);
                        } catch (InterruptedException ex) {
                        }
                    }
                }
                switch (pAnimation) {
                    case 0:
                        doCompanyAnimation();
                        break;
                    case 1:
                        doAdminAnimation();
                        break;
                    default:
                        doPointAnimation();
                        break;
                }
            }
        }.start();
    }

