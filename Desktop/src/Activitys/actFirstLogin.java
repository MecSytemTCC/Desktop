    private void btnProntoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnProntoMouseClicked
        if (doCompanyValidations()) {
            Company c = new Company();
            c.setNameCompany(edtCompanyName.getText());
            c.setCPNJ(edtCNPJ.getText());
            c.setNumberCompany(edtNumber.getText());
            c.setStreetName(edtStreet.getText());
            if (c.Create()) {
                lblTitle.setText("Agora vamos cadastrar um Administrador!");
                lblSubTitle.setText("Informe seus dados");
                doAnimation(1);
            }
        }
    }//GEN-LAST:event_btnProntoMouseClicked

