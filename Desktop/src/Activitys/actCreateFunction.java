    public boolean CheckIsEmpty() {
        if ((edtFunction.getText().trim().isEmpty() || edtFunction.getText().equals("Nome da Função"))) {
            return false;
        }  else if (edtCBO.getText().trim().length() != 7) {
            return false;
        }
        return true;
    }

    public boolean CheckIfExists() {
        Function f = new Function();
        f.setCBO(edtCBO.getText());
        f.ReadByCBO();
        return f.getFunctionName() == null;
    }

    public boolean doValidations() {
        if (!CheckIsEmpty()) {
            new actAlert().setVisible(true);
            return false;
        } else if (!CheckIfExists()) {
            actAlert a = new actAlert();
            a.lbMsg.setText("<html><p align='center'>Esse CBO já está <br> Cadastrado para outra Função!</p></html>");
            a.setVisible(true);
            return false;
        }
        return true;
    }

