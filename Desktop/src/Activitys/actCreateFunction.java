    private void btnProntoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnProntoMouseClicked
        if (doValidations()) {
            Function f = new Function();
            f.setCBO(edtCBO.getText());
            f.setFunctionName(edtFunction.getText());
            if (f.Create()) {
                actAlert alert = new actAlert();
                alert.lbMsg.setText("<html><p align=\"center\">Função cadastrada com sucesso!</p></html>");
                alert.setVisible(true);
                edtCBO.setText("");
                edtFunction.setText("Nome da Função");
            } else {
                actAlert alert = new actAlert();
                alert.lbMsg.setText("<html><p align=\"center\">Esse Nome já está <br> Cadastrado para outra Função!</p></html>");
                alert.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnProntoMouseClicked

