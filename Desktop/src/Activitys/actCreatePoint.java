    public boolean CheckIsEmpty() {
        if ((edtLocale.getText().trim().isEmpty() || edtLocale.getText().equals("Local"))) {
            return false;
        }  else if ((edtCode.getText().trim().isEmpty() || edtCode.getText().equals("Código"))) {
            return false;
        }
        return true;
    }
    
    public boolean CheckNumber(String TestingNumber) {
        int Number = 0;
        try {
            Number = Integer.parseInt(TestingNumber);
        } catch (NumberFormatException e) {
            return true;
        }
        return false;
    }

    

    public boolean doValidations() {
        if (!CheckIsEmpty()) {
            new actAlert().setVisible(true);
            return false;
        } else if (CheckNumber(edtCode.getText())){
           actAlert a = new actAlert();
           a.lbMsg.setText("<html><p align=\"center\">O Código <br> precisa ser um Número!</p></html>");
           a.setVisible(true);
        }
        return true;
    }

