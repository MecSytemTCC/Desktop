    private void btnProntoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnProntoMouseClicked
        if (doValidations()) {
            Point p = new Point();
            p.setCode(Integer.parseInt(edtCode.getText()));
            p.setLocale(edtLocale.getText());
            if(p.Create()){
                actAlert a = new actAlert();
                a.lbMsg.setText("<html><p align=\"center\">Ponto cadastrado com sucesso!</p></html>");
                a.setVisible(true);
                edtCode.setText("Código");
                edtLocale.setText("Local");
            }
        }
    }//GEN-LAST:event_btnProntoMouseClicked

